import streamlit as st
import pandas as pd
import joblib

rf = joblib.load('models/salary_predictor.pkl')
title_encoder = joblib.load('models/title_encoder.pkl')
company_encoder = joblib.load('models/company_encoder.pkl')
category_encoder = joblib.load('models/category_encoder.pkl')
state_encoder = joblib.load('models/state_encoder.pkl')
county_encoder = joblib.load('models/county_encoder.pkl')

df = pd.read_csv('data/jobs.csv')

title_dropdown = st.selectbox(
    options=sorted(title_encoder.classes_),
    label = 'Job title',
    placeholder= 'Title:'
)
company_dropdown = st.selectbox(
    options=sorted(company_encoder.classes_),
    label = 'company',
    placeholder= 'company'
)

category_dropdown = st.selectbox(
    options=sorted(category_encoder.classes_),
    label = 'category',
    placeholder = 'category'
)

state_dropdown = st.selectbox(
    options=sorted([x for x in state_encoder.classes_ if x is not None]),
    label = 'state',
    placeholder= 'State'
)
county_dropdown = st.selectbox(
    options=[],
    label = 'County',
    placeholder= 'County:'

)

company = st.selectbox(
    "Company",
    sorted(company_encoder.classes_)
)

filtered_titles = (
    df.loc[df["company"] == company, "title"]
      .dropna()
      .unique()
)

title = st.selectbox(
    "Title",
    sorted(filtered_titles)
)

state = st.selectbox(
    "State",
    sorted(df["state"].dropna().unique())
)

filtered_counties = (
    df.loc[df["state"] == state, "county"]
      .dropna()
      .unique()
)

county = st.selectbox(
    "County",
    sorted(filtered_counties)
)


filtered_categories = (
    df.loc[
        (df["company"] == company) &
        (df["title"] == title),
        "category"
    ]
    .dropna()
    .unique()
)

category = st.selectbox(
    "Category",
    sorted(filtered_categories)
)

# Display selections
st.write("Company:", company)
st.write("Title:", title)
st.write("State:", state)
st.write("County:", county)
st.write("Category:", category)

st.write(df.columns)

st.write(df[["state", "county"]].head(20))

st.write(df["state"].dtype)
st.write(df["county"].dtype)

st.write(df["state"].nunique())
st.write(df["county"].nunique())

st.write(state)
st.write(df[df["state"] == state].shape)

